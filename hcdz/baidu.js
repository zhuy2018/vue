function carClick(){
  if($('.index_gj ul li:eq(0)').hasClass('active1')){
      map.addOverlay(labelDc);
      map.addOverlay(labelDc1);
      map.addOverlay(labelDc2); 
  }else{
      map.removeOverlay(labelDc);
      map.removeOverlay(labelDc1);
      map.removeOverlay(labelDc2);
  }

  if($('.index_gj ul li:eq(1)').hasClass('active1')){
    map.addOverlay(labelGjc);
    map.addOverlay(labelGjc1);  
  }else{
    map.removeOverlay(labelGjc);
    map.removeOverlay(labelGjc1);
  }

  if($('.index_gj ul li:eq(2)').hasClass('active1')){
    map.addOverlay(labelDb); 
  }else{
    map.removeOverlay(labelDb);
  }

  if($('.index_gj ul li:eq(3)').hasClass('active1')){
    if(_hc==0){
      map.removeOverlay(labelCzc2);
      map.removeOverlay(labelCzc3);
      map.addOverlay(labelCzc);
      map.addOverlay(labelCzc1);
    }else{
      map.removeOverlay(labelCzc);
      map.removeOverlay(labelCzc1);
      map.addOverlay(labelCzc2);
      map.addOverlay(labelCzc3);
    }
  }else{
    if(_hc==0){
      map.removeOverlay(labelCzc);
      map.removeOverlay(labelCzc1);
    }else{
      map.removeOverlay(labelCzc2);
      map.removeOverlay(labelCzc3);
    }
  }

  if($('.index_gj ul li:eq(4)').hasClass('active1')){
    map.addOverlay(labelShcl);
    map.addOverlay(labelShcl1);   
  }else{
    map.removeOverlay(labelShcl);
    map.removeOverlay(labelShcl1);
  }
}

